#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t cur;

	if (s && f)
	{
		cur = 0;
		while (s[cur] != '\0')
		{
			(*f)(s + cur);
			cur++;
		}
	}
}
